 #include<iostream>
 using namespace std;   
 //printing square pattern:
 
 int main()
 {  
  int n;
  cout<<"\n PATTERN PROGRAMMING: ";
  cout<<"\nEnter the number till where u want to print:";
  cin>>n;
 //printing for row 
  for(int i=0;i<n;i++)
 {
  //printing for columns
  for(int j=0;j<n;j++)
 {
   cout<<"*";
 }
 cout<<endl;
 }
 return 0;
 }

  
  
  

